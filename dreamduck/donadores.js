// Crear un objeto para almacenar las donaciones agrupadas por nombre
let donacionesAgrupadas = {};
let total=0;
// Agrupar las donaciones por nombre y sumarlas
donadores.forEach(donador => {
    total+=donador.Donaciones
    if (donacionesAgrupadas.hasOwnProperty(donador.nombre)) {
        donacionesAgrupadas[donador.nombre] += donador.Donaciones;
    } else {
        donacionesAgrupadas[donador.nombre] = donador.Donaciones;
    }
});

// Convertir el objeto en un arreglo de objetos para facilitar su manipulación
let donadoresAgrupados = Object.keys(donacionesAgrupadas).map(nombre => {
    return { nombre: nombre, Donaciones: donacionesAgrupadas[nombre] };
});

// Ordenar donadores por cantidad de donaciones (de mayor a menor)
donadoresAgrupados.sort((a, b) => b.Donaciones - a.Donaciones);

// Obtener la referencia de la tabla
let tabla = document.getElementById("tabla-donaciones");
let totalh4 = document.getElementById("total");
let progress=document.getElementById("progress");
totalh4.innerText="$"+total.toFixed(2)+" de $500";
let porcentaje = (total / 500) * 100;
progress.style.width= porcentaje+"%";
// Iterar sobre los donadores y agregarlos a la tabla
donadoresAgrupados.forEach((donador, index) => {
    let fila = document.createElement("tr");
    fila.innerHTML = `
        <td>${index + 1}</td>
        <td>${donador.nombre}</td>
        <td>$${donador.Donaciones.toFixed(2)}</td>
    `;
    tabla.appendChild(fila);
});